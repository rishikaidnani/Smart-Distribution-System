    public void populateVaccine() {
        vaccineJComboBox.removeAllItems();

        for (VaccineManufacturerEnterprise vmEnterprise : cdcEnterprise.getVaccineManufacturerList()) {
            for (Vaccine vaccine : vmEnterprise.getVaccineCatalog().getVaccineList()) {
                vaccineJComboBox.addItem(vaccine);
            }
        }
    }

