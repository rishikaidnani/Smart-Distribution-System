    private void vaccineJComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vaccineJComboBoxActionPerformed
        // TODO add your handling code here:
        Vaccine vaccine = (Vaccine) vaccineJComboBox.getSelectedItem();

        DefaultTableModel model = (DefaultTableModel) doseJTable.getModel();
        model.setRowCount(0);

        for (SiteEnterprise en : enterprise.getSiteList()) {
            for (Organization o : en.getOrganizationDirectory().getOrganizationList()) {
                if (o instanceof SiteOperationOrganization) {
                    SiteOperationOrganization organization = (SiteOperationOrganization) o;

                    for (OrderItem oi : organization.getVaccineInventory()) {
                        if (oi.getVaccine() == vaccine && oi.getTotalQuantity() > 0 && oi.getExpiryDate().after(new Date())) {
                            Object row[] = new Object[4];
                            row[0] = en;
                            row[1] = oi.getTotalQuantity();
                            row[2] = oi.getVaccine().getDisease();
                            row[3] = oi.getProvider();
                            model.addRow(row);
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_vaccineJComboBoxActionPerformed

