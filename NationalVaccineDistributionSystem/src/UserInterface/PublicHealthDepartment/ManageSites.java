    public void populateEnterpriseTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();
        model.setRowCount(0);

        for (SiteEnterprise enterprise : enterprise.getSiteList()) {
            Object row[] = new Object[4];
            row[0] = enterprise.getTypeOfSite();
            row[1] = enterprise;
            row[2] = enterprise.getStateName();
            row[3] = enterprise.getPinCode();
            model.addRow(row);
        }
    }

