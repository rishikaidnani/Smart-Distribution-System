    private void addEnterpriseJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEnterpriseJButtonActionPerformed
        // TODO add your handling code here:
        int pinCode = Integer.parseInt(String.valueOf(pinJComboBox.getSelectedItem()));
        String siteType = String.valueOf(siteTypeJComboBox.getSelectedItem());
        String siteName = enterpriseNameJTextField.getText();

        if (siteName.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter the name!");
            return;
        }

        if (siteType.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Select site type!");
            return;
        }

        Enterprise e = null;
        e = network.getEnterpriseDirectory().addAndCreateSiteEnterprise(siteType, siteName, stateName, pinCode);
        if (e == null) {
            JOptionPane.showMessageDialog(null, "Enterprise not added!!");
            return;
        }
        enterprise.getSiteList().add((SiteEnterprise) e);
        enterpriseNameJTextField.setText("");
        populateEnterpriseTable();
    }//GEN-LAST:event_addEnterpriseJButtonActionPerformed

    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed

    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterpriseJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Kindly select a row");
            return;
        }

        SiteEnterprise siteEnterprise = (SiteEnterprise) enterpriseJTable.getValueAt(selectedRow, 1);
        network.getEnterpriseDirectory().removeEnterpriseObject(siteEnterprise);
        enterprise.getSiteList().remove(siteEnterprise);
        populateEnterpriseTable();
    }//GEN-LAST:event_removeJButtonActionPerformed


