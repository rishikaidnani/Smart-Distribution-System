    public void populateEnterpriseTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();
        model.setRowCount(0);

        for (PHDEnterprise enterprise : enterprise.getPhdList()) {
            Object row[] = new Object[3];
            row[0] = enterprise.getTypeOfEnterprise();
            row[1] = enterprise;
            row[2] = enterprise.getStateName();
            model.addRow(row);
        }

        for (VaccineManufacturerEnterprise enterprise : enterprise.getVaccineManufacturerList()) {
            Object row[] = new Object[3];
            row[0] = enterprise.getTypeOfEnterprise();
            row[1] = enterprise;
            row[2] = "Not Applicable";
            model.addRow(row);
        }
    }

