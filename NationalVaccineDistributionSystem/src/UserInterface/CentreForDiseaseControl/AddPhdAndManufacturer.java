    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterpriseJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Kindly select an enterprise!");
            return;
        }
        Enterprise choseEnterprise = (Enterprise) enterpriseJTable.getValueAt(selectedRow, 1);
        Enterprise.TypeOfEnterprise type = (Enterprise.TypeOfEnterprise) enterpriseJTable.getValueAt(selectedRow, 0);

        network.getEnterpriseDirectory().removeEnterpriseObject(choseEnterprise);
        if(type.getValue().equals(Enterprise.TypeOfEnterprise.PHD.getValue())){
        enterprise.getPhdList().remove(choseEnterprise);
        } else if(type.getValue().equals(Enterprise.TypeOfEnterprise.VaccineManufacturer.getValue())){
        enterprise.getVaccineManufacturerList().remove(choseEnterprise);
        }
        populateEnterpriseTable();
    }//GEN-LAST:event_removeJButtonActionPerformed


