    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) vaccineAvailableJTable.getModel();
        model.setRowCount(0);

        for (Vaccine vaccine : enterprise.getVaccineInventory().getVaccineInventoryList()) {
            if (vaccine.getBatchAvailability() > 0) {
                Object row[] = new Object[4];
                row[0] = vaccine;
                row[1] = vaccine.getBatchAvailability();
                row[2] = vaccine.getManufacturingBatch();
                row[3] = vaccine.getExpiryDate();
                model.addRow(row);
            }
        }
    }

    public void populateVaccineTable() {
        DefaultTableModel model = (DefaultTableModel) vaccineJTable.getModel();
        model.setRowCount(0);

        for (Vaccine vaccine : enterprise.getVaccineCatalog().getVaccineList()) {
            Object row[] = new Object[4];
            row[0] = vaccine;
            row[1] = vaccine.getSerialNo();
            row[2] = vaccine.getPrice();
            model.addRow(row);
        }
    }

