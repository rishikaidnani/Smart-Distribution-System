    private void setJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setJButtonActionPerformed
        // TODO add your handling code here:
        int vaccineAvailability;
        try {
            vaccineAvailability = Integer.parseInt(availableJTextField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Kindly input proper values");
            return;
        }

        int selectedRow = vaccineJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Kindly select a row");
            return;
        }

        Vaccine vaccine = (Vaccine) vaccineJTable.getValueAt(selectedRow, 0);
        Vaccine vaccineForSale = enterprise.getVaccineInventory().addVaccineInInventory(vaccine, vaccineAvailability);
        availableJTextField.setText("");
        populateVaccineAvailableTable(vaccineForSale);

//*************Set state level allocation******************
        ArrayList<VaccineAllocation> vaccineAllocationData = vaccineForSale.getVaccineAllocationData();
        vaccineAllocationData = new ArrayList<>();
        vaccineForSale.setVaccineAllocationData(vaccineAllocationData);
        for (PHDEnterprise phdEnterprise : cdcEnterprise.getPhdList()) {
            float populationPercentile = phdEnterprise.setAllocatedPercentileForState(phdEnterprise.getStateName());
            float allocatedQuantity = (populationPercentile / 100) * vaccineAvailability;
            VaccineAllocation vaccineAllocation = new VaccineAllocation();
            vaccineAllocation.setPhdEnterprise(phdEnterprise);
            vaccineAllocation.setAllocatedVaccineQuantity(allocatedQuantity);
            vaccineAllocationData.add(vaccineAllocation);
        }
//*******************************************************************
    }//GEN-LAST:event_setJButtonActionPerformed

