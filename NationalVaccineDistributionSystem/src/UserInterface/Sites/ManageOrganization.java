    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        model.setRowCount(0);

        for (Organization organization : siteEnterprise.getOrganizationDirectory().getOrganizationList()) {
            Object row[] = new Object[2];
            row[0] = organization.getName();
            row[1] = organization.getType();

            model.addRow(row);
        }
    }

    public void populateOrganizationComboBox() {
        organizationTypeJComboBox.removeAllItems();
        for (Organization.TypeOfOrganization type : Organization.TypeOfOrganization.values()) {
            if (type == Organization.TypeOfOrganization.SiteOperation
                    || type == Organization.TypeOfOrganization.ProviderOrganization) {
                organizationTypeJComboBox.addItem(type);
            }
        }
    }

