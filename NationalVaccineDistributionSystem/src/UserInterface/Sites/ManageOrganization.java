    private void addOrganizationJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrganizationJButtonActionPerformed
        // TODO add your handling code here:
        String organizationName = organizationNameJTextField.getText();

        if (organizationName.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Kindly input organization name");
            return;
        }

        Organization.TypeOfOrganization type = (Organization.TypeOfOrganization) organizationTypeJComboBox.getSelectedItem();

        Organization o = siteEnterprise.getOrganizationDirectory().addOrganization(type, organizationName);
        if (o == null) {
            JOptionPane.showMessageDialog(null, "Organization already exists!");
            return;
        }
        organizationNameJTextField.setText("");
        populateTable();
    }//GEN-LAST:event_addOrganizationJButtonActionPerformed

    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed

    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = organizationJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Kindly select the warehouse");
            return;
        }

        Organization organization = (Organization) organizationJTable.getValueAt(selectedRow, 0);

        siteEnterprise.getOrganizationDirectory().removeOrganization(organization);
        populateTable();
    }//GEN-LAST:event_removeJButtonActionPerformed


